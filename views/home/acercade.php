<br><h4><b>Historia</b></h4><br>
<p>
Originalmente creado por un diseñador y desarrollador en Twitter, Bootstrap se ha convertido en uno de los frameworks front-end y proyectos de código abierto más populares del mundo.

Bootstrap fue creado en Twitter a mediados de 2010 por @mdo y @fat . Antes de ser un marco de código abierto, Bootstrap era conocido como Blueprint de Twitter . A los pocos meses de desarrollo, Twitter celebró su primera Hack Week y el proyecto explotó cuando los desarrolladores de todos los niveles de habilidades saltaron sin ninguna orientación externa. Sirvió como guía de estilo para el desarrollo de herramientas internas en la empresa durante más de un año antes de su lanzamiento público, y continúa haciéndolo hoy.

Lanzado originalmente elViernes 19 de agosto de 2011, desde entonces hemos tenido más de veinte lanzamientos , incluidas dos reescrituras principales con v2 y v3. Con Bootstrap 2, agregamos una funcionalidad receptiva a todo el marco como una hoja de estilo opcional. Partiendo de eso con Bootstrap 3, reescribimos la biblioteca una vez más para que responda de forma predeterminada con un primer enfoque móvil.

Con Bootstrap 4, una vez más reescribimos el proyecto para tener en cuenta dos cambios arquitectónicos clave: una migración a Sass y el traslado a la caja flexible de CSS. Nuestra intención es ayudar de una pequeña manera a impulsar la comunidad de desarrollo web impulsando propiedades CSS más nuevas, menos dependencias y nuevas tecnologías en navegadores más modernos.

</p>

